            break;

        case COMM_A_POS_C_NEG:
            __HAL_TIM_SET_COMPARE(&htim1, TIM_CHANNEL_1, duty); // A+
            __HAL_TIM_SET_COMPARE(&htim1, TIM_CHANNEL_3, duty); // Câˆ’
